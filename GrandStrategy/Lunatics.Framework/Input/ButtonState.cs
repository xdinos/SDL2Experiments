namespace Lunatics.Framework.Input
{
	/// <summary>
	/// Defines a button state for buttons of mouse, gamepad or joystick.
	/// </summary>
	public enum ButtonState
	{
		/// <summary>
		/// The button is released.
		/// </summary>
		Released,
		/// <summary>
		/// The button is pressed.
		/// </summary>
		Pressed
	}
}